"""
Council Command Center - Dashboard Sync

Reads all state files and generates dashboard/data.js
Run this after any state change to keep dashboard current.
"""

import json
import os
from pathlib import Path
from datetime import datetime

# Import state functions
from update_state import get_dashboard_data, _now_iso

# Paths
COUNCIL_DIR = Path(__file__).parent.parent
DASHBOARD_DIR = COUNCIL_DIR.parent / "dashboard"
DATA_JS_FILE = DASHBOARD_DIR / "data.js"


def sync_dashboard() -> dict:
    """
    Sync state to dashboard/data.js
    
    Returns:
        The synced data
    """
    # Get merged state
    data = get_dashboard_data()
    
    # Ensure dashboard directory exists
    DASHBOARD_DIR.mkdir(exist_ok=True)
    
    # Generate JavaScript module format
    js_content = f"""// Auto-generated by sync_dashboard.py
// Last sync: {_now_iso()}
// DO NOT EDIT MANUALLY

window.COUNCIL_DATA = {json.dumps(data, indent=2)};

// For ES module compatibility
if (typeof module !== 'undefined') {{
  module.exports = window.COUNCIL_DATA;
}}
"""
    
    # Write to data.js
    with open(DATA_JS_FILE, 'w') as f:
        f.write(js_content)
    
    print(f"âœ… Synced dashboard data to {DATA_JS_FILE}")
    print(f"   Balance: ${data['balance']} / ${data['target']} ({data['progress']}%)")
    print(f"   Pipeline: {sum(data['pipelineCounts'].values())} opportunities")
    print(f"   Feed entries: {len(data['feed'])}")
    
    return data


